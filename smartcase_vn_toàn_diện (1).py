def format_date_vn(date_obj):
    return date_obj.strftime("%d/%m/%Y")

def parse_date_vn(date_str):
    try: return datetime.strptime(date_str, "%d/%m/%Y")
    except: return datetime.now()

def get_base64_image(file):
    if file: return f"data:image/png;base64,{base64.b64encode(file.getvalue()).decode()}"
    return None

def call_gemini_api(prompt):
    api_key = "" # API Key provided by runtime
    url = f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash-preview-09-2025:generateContent?key={api_key}"
    headers = {'Content-Type': 'application/json'}
    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"responseMimeType": "application/json"}
    }
    for delay in [1, 2, 4, 8]:
        try:
            res = requests.post(url, headers=headers, json=payload)
            if res.status_code == 200:
                return res.json()['candidates'][0]['content']['parts'][0]['text']
        except:
            time.sleep(delay)
    return "{}"

