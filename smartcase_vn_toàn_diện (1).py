def render_dashboard():
    st.header(":material/dashboard: Bảng Điều Khiển Hệ Thống")
    st.divider()

    active_cases = len([c for c in st.session_state.clients if c['status'] != 'Đã đóng ca'])
    urgent_cases = len([c for c in st.session_state.clients if c['riskLevel'] == 'Khẩn cấp' and c['status'] != 'Đã đóng ca'])

    col1, col2, col3 = st.columns(3)
    col1.metric("Tổng số ca đang quản lý", active_cases)
    col2.metric("Ca báo động Khẩn cấp", urgent_cases)
    col3.metric("Mục tiêu hoàn thành", "75%")

    st.subheader(":material/notifications_active: Chuông báo Hệ thống")
    warnings_found = False

    for client in st.session_state.clients:
        if client['status'] == 'Đã đóng ca': continue
        for goal in client['details'].get('goals', []):
            if goal['status'] == 'Hoàn thành': continue

            d_date = parse_date_vn(goal['deadline'])
            diff_days = (d_date - datetime.now()).days

            if diff_days <= 7:
                warnings_found = True
                is_overdue = diff_days < 0
                bg_color = "badge-red" if is_overdue else "badge-yellow"
                icon = "🚨" if is_overdue else "⚠️"
                text = f"Quá hạn {abs(diff_days)} ngày" if is_overdue else (f"Còn {diff_days} ngày" if diff_days > 0 else "Hết hạn hôm nay")

                with st.container(border=True):
                    w_col1, w_col2 = st.columns([4, 1])
                    with w_col1:
                        st.markdown(f"**[{client['id']}] {client['alias']}** • {client['category']}")
                        st.write(f"Mục tiêu: {goal['text']}")
                        st.markdown(f"<span class='badge {bg_color}'>{icon} {text} (Hạn: {goal['deadline']})</span>", unsafe_allow_html=True)
                    with w_col2:
                        if st.button("Cập nhật ngay", key=f"warn_btn_{goal['id']}", use_container_width=True):
                            st.session_state.selected_client_id = client['id']
                            st.session_state.current_page = "Detail"
                            st.rerun()

    if not warnings_found:
        st.success("Tuyệt vời! Không có mục tiêu nào bị quá hạn hoặc sắp hết hạn.")

